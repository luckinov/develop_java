package week7;

public class Vechicle {
    String m;
    String model;
    int y;

    Vechicle() {
        m = "hhh0";
        model = "11451";
        y = 2020;
    }

    public String getM() {
        return m;
    }

    public String getModel() {
        return model;
    }
    

    public int getY() {
        return y;
    }

    public void setM(String m) {
        this.m = m;
    }

    public void setModel(String model) {
        this.model = model;
    }
    public void setY(int y) {
        this.y = y;
    }
}
